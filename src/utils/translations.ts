// Function to get translation with fallback to English
export const getTranslation = (text: string, targetLang: string): string => {
  if (targetLang === 'en') {
    return text; // Return original text if target language is English
  }

  const key = text.trim();
  if (translations[key] && translations[key][targetLang]) {
    return translations[key][targetLang];
  }

  // If translation not found, return original text (fallback)
  // This maintains the original English text when no translation is available
  console.warn(`Translation not found for: "${text}" in language: "${targetLang}"`);
  return text;
};

// Cache for translateText function to avoid repeated processing of the same text blocks
const textTranslationCache = new Map<string, Map<string, string>>();

// Function to get cached text translation or compute and cache it
const getCachedTextTranslation = (text: string, targetLang: string): string => {
  if (!textTranslationCache.has(targetLang)) {
    textTranslationCache.set(targetLang, new Map<string, string>());
  }

  const cacheKey = `text:${text}`;
  const langCache = textTranslationCache.get(targetLang)!;
  if (langCache.has(cacheKey)) {
    return langCache.get(cacheKey)!;
  }

  // Perform the actual translation
  let result: string;
  if (targetLang === 'en') {
    result = text;
  } else {
    // Split text into sentences/fragments and translate each
    const sentences = text.split(/([.!?]+)/);
    const translatedSentences = sentences.map(sentence => {
      if (sentence.trim() && sentence.trim() !== '' && sentence.trim() !== '.') {
        return getTranslation(sentence.trim(), targetLang);
      }
      return sentence;
    });

    result = translatedSentences.join('');
  }

  langCache.set(cacheKey, result);
  return result;
};

// Function to translate an entire block of text with fallback and caching
export const translateText = (text: string, targetLang: string): string => {
  return getCachedTextTranslation(text, targetLang);
};

// Function to translate multiple texts
export const translateMultiple = (texts: string[], targetLang: string): string[] => {
  return texts.map(text => getTranslation(text, targetLang));
};

